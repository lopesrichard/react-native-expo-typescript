import React, { FunctionComponent } from 'react';

import { View } from './view';

// ====================================================================================================================
// =============================================== TYPES AND INTERFACES ===============================================
// ====================================================================================================================

export type Row = View & {
  reverse?: boolean;
};

// ====================================================================================================================
// ==================================================== COMPONENTS ====================================================
// ====================================================================================================================

export const Row: FunctionComponent<Row> = props => {
  return <View {...{ ...props, fd: props.reverse ? 'row-reverse' : 'row' }} />;
};
